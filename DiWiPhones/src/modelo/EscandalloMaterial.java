package modelo;

public class EscandalloMaterial {
	private int idEscandallo;
	private int unidades;
	private int escandallMaterial;
	private int material;
	private String nombre;
	
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getIdEscandallo() {
		return idEscandallo;
	}
	public void setIdEscandallo(int idEscandallo) {
		this.idEscandallo = idEscandallo;
	}
	public int getUnidades() {
		return unidades;
	}
	public void setUnidades(int unidades) {
		this.unidades = unidades;
	}
	public int getEscandallMaterial() {
		return escandallMaterial;
	}
	public void setEscandallMaterial(int escandallMaterial) {
		this.escandallMaterial = escandallMaterial;
	}
	public int getMaterial() {
		return material;
	}
	public void setMaterial(int material) {
		this.material = material;
	}
	
	
	

}
